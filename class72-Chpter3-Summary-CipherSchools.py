# if statement
name="harshit"
if name == "harshit":
    print("you are harshit")
else:
    print("you are not harshit")

# while
i=0
while i<10:
    print("hello world")
    print(i)
    i += 1

# for Loop
for i in range(1,11,2):
    print(i)

# break keyword
for i in range(1,11):
    if i==5:
        break
    print(i)

# continue keyword
for i in range(1,11):
    if i==5:
        continue
    print(i)
